let web3;
let contract;
let accounts;

document.getElementById('connectButton').onclick = async () => {
    if (typeof window.ethereum !== 'undefined') {
        try {
            await ethereum.request({ method: 'eth_requestAccounts' });
            web3 = new Web3(window.ethereum);
            accounts = await web3.eth.getAccounts();
            document.getElementById('account').innerText = `Connected: ${accounts[0]}`;
            contract = new web3.eth.Contract(contractABI, contractAddress);
            document.getElementById('mintButton').disabled = false;
            console.log('Web3 instance:', web3);
            console.log('Contract instance:', contract);
            console.log('Wallet connected:', accounts[0]);
        } catch (error) {
            console.error('User rejected the request', error);
        }
    } else {
        console.error('MetaMask is not installed');
    }
};


document.getElementById('mintButton').onclick = async () => {
    console.log('Mint buton clicked');
    const fileInput = document.getElementById('fileInput');
    if (fileInput.files.length === 0) {
        alert('Please select a file.');
        return;
    }
    
    const file = fileInput.files[0];
    console.log('File selected', file);
    try {
        const tokenURI = await uploadFileToPinata(file);
        console.log('File uploaded to Pinata:', tokenURI);
        const receipt = await contract.methods.mintNFT(accounts[0], tokenURI).send({ from: accounts[0] });
        console.log('NFT Minted:', receipt);
    } catch (error) {
        console.error('Minting failed:', error);
    }
};

async function uploadFileToPinata(file) {
    const url = `https://api.pinata.cloud/pinning/pinFileToIPFS`;
    const formData = new FormData();
    formData.append('file', file);

    const response = await fetch(url, {
        method: 'POST',
        headers: {
            'pinata_api_key': pinataApiKey,
            'pinata_secret_api_key': pinataSecretApiKey
        },
        body: formData
    });

    const result = await response.json();
    return `ipfs://${result.IpfsHash}`;
}
